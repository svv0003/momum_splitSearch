    if (provinces.containsKey(region)) {
      return provinces[region];
    }

    for (final province in allRegions.keys) {
      if (region.startsWith(province) || region.endsWith(province)) {
        final details = allRegions[province]!;

        for (final detail in details.keys) {
          if (region.contains(' $detail') || region.endsWith(detail)) {
            return details[detail];
          }
        }

        return provinces[province];
      }
    }

    for (final details in allRegions.values) {
      if (details.containsKey(region)) {
        return details[region];
      }
    }

    return null;
  }
